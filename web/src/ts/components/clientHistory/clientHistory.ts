import $ from "jquery";
import { ClientAction, ClientActionMap } from "./types";

export default function renderClientHistory(
	container: HTMLElement,
	history: ClientAction<keyof ClientActionMap>[]
) {
	const $container = $(container);
	$container.addClass("client-history");
	history.forEach((action) => {
		const $el = $(document.createElement("div"));
		
		$el.addClass("client-action");
		
		$el.append(icons[action.type]);
		$el.append(
			`<time datetime=${action.timestamp.toISOString()}>${action.timestamp.toLocaleString()}</time>`
		);

		if (action.type === "created") {
			$el.append(`<p>Клиент добавлен</p>`);
		} else if (action.type === "deleted") {
			const deletedAction = action as ClientAction<"deleted">;
			$el.append(
				`<p>Клиент удален <span>(${deletedAction.payload.reason})</span></p>`
			);
		} else if (action.type === "contractEstablished") {
			const contractEstablishedAction =
				action as ClientAction<"contractEstablished">;
			$el.append(
				`<p>Заключена сделка <span>#${contractEstablishedAction.payload.contractId}</span></p>`
			);
		} else if (action.type === "meeting") {
			const meetingAction = action as ClientAction<"meeting">;
			$el.append(
				`<p>Проведен созвон <span>#${meetingAction.payload.meetingId}</span></p>`
			);
		}
		$container.append($el);
	});
}

